#!/usr/bin/env python3
"""Run the supplied modeling orchestrator from the tracked processed snapshot."""

from __future__ import annotations

import argparse
import shutil
import subprocess
import sys

import _bootstrap

from ecb_vol_forecasting.config import load_config
from ecb_vol_forecasting.logging_utils import configure_logging


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--quick", action="store_true")
    parser.add_argument("--skip-oos", action="store_true")
    args = parser.parse_args()
    log = configure_logging()
    config = load_config()
    output = _bootstrap.ROOT / "output"
    output.mkdir(exist_ok=True)
    shutil.copy2(config.paths.master_dataset, output / "model_data_master.csv")

    command = [
        sys.executable,
        str(config.paths.legacy_scripts / "run_main_pipeline.py"),
        "--include-appendix",
    ]
    if args.quick:
        command.append("--quick-oos")
    if args.skip_oos:
        command.append("--skip-oos")
    log.info("Running supplied modeling orchestrator in legacy-compatible output/.")
    subprocess.run(command, cwd=_bootstrap.ROOT, check=True)

    report_script = config.paths.legacy_scripts / "06_make_report_artifacts.py"
    if report_script.exists():
        subprocess.run([sys.executable, str(report_script)], cwd=_bootstrap.ROOT, check=True)
    log.warning(
        "This recreates the supplied modeling workflow from the processed snapshot. "
        "Non-convex NA-GARCH fits can reach different local optima, and the supplied "
        "merged Bernoth appendix is not regenerated by the orchestrator."
    )


if __name__ == "__main__":
    main()
